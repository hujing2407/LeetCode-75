package ESDC_IITB_IT01.BenefitFinder;

/**
 * The Benefit Interface represent a benefit.
 * Any class that implements this interface should provide an implementation
 * for the benefit method, which output the benefit name.
 *
 * @author Jing Hu
 * @date Apr. 13 2023
 */
public interface Benefit {

    /**
     * This benefit method could print out the benefit name the citizen deserves
     */
    void benefit();
}
